export function getPrintLocaleOptions(siteLocale: SiteLocale) {
  return printLocales.map((value) => ({
    label: printLocaleLabels[siteLocale][value],
    value,
  }));
}

export function resolveLocalizedText(text: LocalizedText | string, locale: PrintLocale, fallbackLocale: PrintLocale = defaultPrintLocale) {
  if (typeof text === "string") {
    return text;
  }

  return text[locale] ?? text[fallbackLocale] ?? text.default;
}

export function isSiteLocale(value: string | null): value is SiteLocale {
  return value === "en" || value === "zh-Hant";
}

export function isPrintLocale(value: string | null): value is PrintLocale {
  return typeof value === "string" && (printLocales as readonly string[]).includes(value);
}

export function getLocaleDirection(locale: SiteLocale | PrintLocale) {
  return locale === "ar" ? ("rtl" as const) : ("ltr" as const);
}

